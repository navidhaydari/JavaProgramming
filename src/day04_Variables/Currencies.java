package day04_Variables;

public class Currencies {
    public static void main(String[] args) {

        // Converting US Dollors to other Currencies.
        int dollars = 100;
        double euro = dollars * 0.95;
        double afn = dollars * 88;
        double canDollars = dollars * 0.77;
        System.out.println("dollars = $" + dollars);
        System.out.println("euro = EUR" + euro);
        System.out.println("afn = AFN" + afn);
        System.out.println("canDollars = CAN" + canDollars);




    }
}
