/*
Create a class named EmployeeInfo declare the following variables:
1. name
2. age
3. gender
4. company
5. jobTitle
6. yearsOfExperience
7. salary
8. isFullTime
9. isMarried
10. employeeId
 */
public class EmployeeInfo {
    public static void main(String[] args) {
        String name = "Navid";
        int age = 26;
        char gender = 'M';
        String company = "Tesla";
        String jobTitle = "QA Tester";
        int yearsOfExperience = 2;
        int salary = 100000;
        boolean isFullTime = true;
        boolean isMarried = false;
        int employeeId = 1234;
        String nie = "Y8895733Z";

        System.out.println("name = " + name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("company = " + company);
        System.out.println("jobTitle = " + jobTitle);
        System.out.println("yearsOfExperience = Y" + yearsOfExperience);
        System.out.println("salary = $" + salary);
        System.out.println("isFullTime = " + isFullTime);
        System.out.println("isMarried = " + isMarried);
        System.out.println("employeeId = " + employeeId);
        System.out.println("nie = " + nie);


    }
}
