package day04_Variables;

public class PrimitiveIntro {
    public static void main(String[] args) {
        // age: 26 years old
        byte age = 26;
        // weight: 160 kg,
        short weight = 160; // because its more than 127 we use short from primitive.
        // salary: 100000 $
        int salary = 100000; // because it is more than 32767 we use int.
        // long for numbers more than 2 billion.
        long asset = 300_000_000_000L; // we put L because java is familiar with int.


    }
}
