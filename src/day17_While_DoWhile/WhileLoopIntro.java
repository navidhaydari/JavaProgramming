package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {
        if (true) {
            System.out.println("wooden spoon"); // its just for one time
        }
        System.out.println("------------------------------------------");
        // just copy if statement because while loop is repeated if statement
        while (true) {
            System.out.println("wooden spoon"); // its infinite time

        }

    }
}
